print(f"Additionen af {num1} og {num2}: {num1 + num2}")  # Addition
print(f"Subtraktionen af {num1} og {num2}: {num1 - num2}")  # Subtraktion
print(f"Multiplikationen af {num1} og {num2}: {num1 * num2}")  # Multiplikation
print(f"Divisionen af {num1} og {num2}: {num1 / num2}")  # Division
